let a = 5;
let b = 10;

// Adição
console.log(`${a} mais ${b} = ${a + b}`);

// Subtração
console.log(`${a} menos ${b} = ${a - b}`);

// Multiplicação
console.log(`${a} vezes ${b} = ${a * b}`);

// Divisão
console.log(`${a} dividido por ${b} = ${a / b}`);

// Módulo
console.log(`${a} dividido por ${b} sobra ${a % b}`);
